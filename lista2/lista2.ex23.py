# Escreva um programa que leia um inteiro entre 1 e 12 e imprima o mês correspondente a este
# número. Isto e, janeiro se é 1, fevereiro é 2, e assim por mesnte.

mes = int(input("Digite o numero do seu mes: "))

if mes == 1:
    print("janeiro!!")
elif mes == 2:
    print("fevereiro!!")
elif mes == 3:
    print("março!!")
elif mes == 4:
    print("abril!!")
elif mes == 5:
    print("maio!!")
elif mes == 6:
    print("junho!!")
elif mes == 7:
    print("julho!!")
elif mes == 8:
    print("agosto!!")
elif mes == 9:
    print("setembro!!")
elif mes == 10:
    print("outubro!!")
elif mes == 11:
    print("novembro!!")
elif mes == 12:
    print("dezembro!!")

else:
    print("Mes invalido!!")


